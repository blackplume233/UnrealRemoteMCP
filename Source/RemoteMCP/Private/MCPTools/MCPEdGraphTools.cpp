// ─────────────────── HandleCompileAsset ───────────────────

FJsonObjectParameter UMCPEdGraphTools::HandleCompileAsset(const FJsonObjectParameter& Params)
{
	FString AssetPath;
	if (!Params->TryGetStringField(TEXT("asset_path"), AssetPath))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'asset_path' parameter"));
	}

	UObject* Asset = LoadObject<UObject>(nullptr, *AssetPath);
	if (!Asset)
	{
		Asset = UEditorAssetLibrary::LoadAsset(AssetPath);
	}
	if (!Asset)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Asset not found: %s"), *AssetPath));
	}

	UBlueprint* Blueprint = Cast<UBlueprint>(Asset);
	if (!Blueprint)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Asset is not a Blueprint: %s (%s)"), *AssetPath, *Asset->GetClass()->GetName()));
	}

	FKismetEditorUtilities::CompileBlueprint(Blueprint);

	FString StatusStr;
	bool bHasError = false;
	switch (Blueprint->Status)
	{
	case BS_UpToDate:
		StatusStr = TEXT("success");
		break;
	case BS_UpToDateWithWarnings:
		StatusStr = TEXT("warning");
		break;
	case BS_Error:
		StatusStr = TEXT("error");
		bHasError = true;
		break;
	case BS_Dirty:
		StatusStr = TEXT("dirty");
		break;
	default:
		StatusStr = TEXT("unknown");
		break;
	}

	TArray<TSharedPtr<FJsonValue>> MessagesArray;

	// Collect compile messages from the Blueprint's MessageLog
	if (Blueprint->CurrentMessageLog)
	{
		for (const TSharedRef<FTokenizedMessage>& Msg : Blueprint->CurrentMessageLog->Messages)
		{
			TSharedPtr<FJsonObject> MsgObj = MakeShared<FJsonObject>();

			switch (Msg->GetSeverity())
			{
			case EMessageSeverity::Error:
				MsgObj->SetStringField(TEXT("severity"), TEXT("error"));
				break;
			case EMessageSeverity::Warning:
			case EMessageSeverity::PerformanceWarning:
				MsgObj->SetStringField(TEXT("severity"), TEXT("warning"));
				break;
			default:
				MsgObj->SetStringField(TEXT("severity"), TEXT("info"));
				break;
			}

			MsgObj->SetStringField(TEXT("message"), Msg->ToText().ToString());
			MessagesArray.Add(MakeShared<FJsonValueObject>(MsgObj));
		}
	}

	TSharedPtr<FJsonObject> ResultObj = MakeShared<FJsonObject>();
	ResultObj->SetStringField(TEXT("asset_path"), AssetPath);
	ResultObj->SetStringField(TEXT("status"), StatusStr);
	ResultObj->SetBoolField(TEXT("has_error"), bHasError);
	ResultObj->SetArrayField(TEXT("messages"), MessagesArray);
	return FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj);
}

// ─────────────────── HandleCreateGraph ───────────────────

FJsonObjectParameter UMCPEdGraphTools::HandleCreateGraph(const FJsonObjectParameter& Params)
{
	FString AssetPath;
	if (!Params->TryGetStringField(TEXT("asset_path"), AssetPath))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'asset_path' parameter"));
	}

	FString GraphName;
	if (!Params->TryGetStringField(TEXT("graph_name"), GraphName))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'graph_name' parameter"));
	}

	FString GraphType = TEXT("function");
	Params->TryGetStringField(TEXT("graph_type"), GraphType);

	UObject* Asset = LoadObject<UObject>(nullptr, *AssetPath);
	if (!Asset) Asset = UEditorAssetLibrary::LoadAsset(AssetPath);
	if (!Asset)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Asset not found: %s"), *AssetPath));
	}

	UBlueprint* Blueprint = Cast<UBlueprint>(Asset);
	if (!Blueprint)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Asset is not a Blueprint"));
	}

	UEdGraph* NewGraph = nullptr;

	if (GraphType.Equals(TEXT("function"), ESearchCase::IgnoreCase))
	{
		NewGraph = FBlueprintEditorUtils::CreateNewGraph(Blueprint, FName(*GraphName), UEdGraph::StaticClass(), UEdGraphSchema_K2::StaticClass());
		if (NewGraph)
		{
			FBlueprintEditorUtils::AddFunctionGraph<UClass>(Blueprint, NewGraph, false, nullptr);
		}
	}
	else if (GraphType.Equals(TEXT("macro"), ESearchCase::IgnoreCase))
	{
		NewGraph = FBlueprintEditorUtils::CreateNewGraph(Blueprint, FName(*GraphName), UEdGraph::StaticClass(), UEdGraphSchema_K2::StaticClass());
		if (NewGraph)
		{
			FBlueprintEditorUtils::AddMacroGraph(Blueprint, NewGraph, false, nullptr);
		}
	}
	else
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Unknown graph_type: %s (supported: function, macro)"), *GraphType));
	}

	if (!NewGraph)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Failed to create graph"));
	}

	Blueprint->MarkPackageDirty();

	TSharedPtr<FJsonObject> ResultObj = MakeShared<FJsonObject>();
	ResultObj->SetStringField(TEXT("graph_name"), NewGraph->GetName());
	ResultObj->SetStringField(TEXT("graph_path"), NewGraph->GetPathName());
	ResultObj->SetStringField(TEXT("graph_class"), NewGraph->GetClass()->GetName());
	ResultObj->SetNumberField(TEXT("node_count"), NewGraph->Nodes.Num());
	return FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj);
}

// ─────────────────── HandleDeleteGraph ───────────────────

FJsonObjectParameter UMCPEdGraphTools::HandleDeleteGraph(const FJsonObjectParameter& Params)
{
	FString AssetPath;
	if (!Params->TryGetStringField(TEXT("asset_path"), AssetPath))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'asset_path' parameter"));
	}

	FString GraphName, GraphPath;
	Params->TryGetStringField(TEXT("graph_name"), GraphName);
	Params->TryGetStringField(TEXT("graph_path"), GraphPath);

	if (GraphName.IsEmpty() && GraphPath.IsEmpty())
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Provide 'graph_name' or 'graph_path'"));
	}

	UObject* Asset = LoadObject<UObject>(nullptr, *AssetPath);
	if (!Asset) Asset = UEditorAssetLibrary::LoadAsset(AssetPath);
	if (!Asset)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Asset not found: %s"), *AssetPath));
	}

	UBlueprint* Blueprint = Cast<UBlueprint>(Asset);
	if (!Blueprint)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Asset is not a Blueprint"));
	}

	UEdGraph* TargetGraph = nullptr;

	if (!GraphPath.IsEmpty())
	{
		TargetGraph = LoadObject<UEdGraph>(nullptr, *GraphPath);
	}

	if (!TargetGraph && !GraphName.IsEmpty())
	{
		auto FindByName = [&GraphName](const auto& Arr) -> UEdGraph* {
			for (UEdGraph* G : Arr)
			{
				if (G && G->GetName() == GraphName) return G;
			}
			return nullptr;
		};

		TargetGraph = FindByName(Blueprint->FunctionGraphs);
		if (!TargetGraph) TargetGraph = FindByName(Blueprint->MacroGraphs);
		if (!TargetGraph) TargetGraph = FindByName(Blueprint->UbergraphPages);
		if (!TargetGraph) TargetGraph = FindByName(Blueprint->EventGraphs);
	}

	if (!TargetGraph)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Graph not found"));
	}

	FString RemovedName = TargetGraph->GetName();
	FBlueprintEditorUtils::RemoveGraph(Blueprint, TargetGraph);
	Blueprint->MarkPackageDirty();

	TSharedPtr<FJsonObject> ResultObj = MakeShared<FJsonObject>();
	ResultObj->SetStringField(TEXT("removed_graph"), RemovedName);
	return FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj);
}

// ─────────────────── HandleGetAssetInfo ───────────────────

FJsonObjectParameter UMCPEdGraphTools::HandleGetAssetInfo(const FJsonObjectParameter& Params)
{
	FString AssetPath;
	if (!Params->TryGetStringField(TEXT("asset_path"), AssetPath))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'asset_path' parameter"));
	}

	UObject* Asset = LoadObject<UObject>(nullptr, *AssetPath);
	if (!Asset) Asset = UEditorAssetLibrary::LoadAsset(AssetPath);
	if (!Asset)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Asset not found: %s"), *AssetPath));
	}

	TSharedPtr<FJsonObject> ResultObj = MakeShared<FJsonObject>();
	ResultObj->SetStringField(TEXT("asset_path"), AssetPath);
	ResultObj->SetStringField(TEXT("asset_class"), Asset->GetClass()->GetName());

	UBlueprint* Blueprint = Cast<UBlueprint>(Asset);
	if (!Blueprint)
	{
		// Non-blueprint asset: just return basic info + graphs
		TArray<TSharedPtr<FJsonValue>> GraphsArray;
		TArray<UObject*> SubObjects;
		GetObjectsWithOuter(Asset, SubObjects, false);
		for (UObject* Obj : SubObjects)
		{
			UEdGraph* G = Cast<UEdGraph>(Obj);
			if (!G) continue;
			TSharedPtr<FJsonObject> GO = MakeShared<FJsonObject>();
			GO->SetStringField(TEXT("name"), G->GetName());
			GO->SetStringField(TEXT("path"), G->GetPathName());
			GO->SetStringField(TEXT("class"), G->GetClass()->GetName());
			GO->SetNumberField(TEXT("node_count"), G->Nodes.Num());
			GraphsArray.Add(MakeShared<FJsonValueObject>(GO));
		}
		ResultObj->SetArrayField(TEXT("graphs"), GraphsArray);
		return FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj);
	}

	// Parent class
	if (Blueprint->ParentClass)
	{
		ResultObj->SetStringField(TEXT("parent_class"), Blueprint->ParentClass->GetName());
		ResultObj->SetStringField(TEXT("parent_class_path"), Blueprint->ParentClass->GetPathName());
	}

	// Blueprint type
	ResultObj->SetStringField(TEXT("blueprint_type"), 
		Blueprint->BlueprintType == BPTYPE_Normal ? TEXT("Normal") :
		Blueprint->BlueprintType == BPTYPE_MacroLibrary ? TEXT("MacroLibrary") :
		Blueprint->BlueprintType == BPTYPE_Interface ? TEXT("Interface") :
		Blueprint->BlueprintType == BPTYPE_FunctionLibrary ? TEXT("FunctionLibrary") :
		TEXT("Other"));

	// Variables
	TArray<TSharedPtr<FJsonValue>> VarsArray;
	for (const FBPVariableDescription& Var : Blueprint->NewVariables)
	{
		TSharedPtr<FJsonObject> VarObj = MakeShared<FJsonObject>();
		VarObj->SetStringField(TEXT("name"), Var.VarName.ToString());
		VarObj->SetStringField(TEXT("type"), Var.VarType.PinCategory.ToString());
		if (Var.VarType.PinSubCategoryObject.IsValid())
		{
			VarObj->SetStringField(TEXT("sub_type"), Var.VarType.PinSubCategoryObject->GetName());
		}
		VarObj->SetStringField(TEXT("category"), Var.Category.ToString());
		VarObj->SetBoolField(TEXT("is_instance_editable"), Var.PropertyFlags & CPF_Edit ? true : false);
		VarObj->SetBoolField(TEXT("is_array"), Var.VarType.IsArray());

		// Export default value text
		if (!Var.DefaultValue.IsEmpty())
		{
			VarObj->SetStringField(TEXT("default_value"), Var.DefaultValue);
		}

		VarsArray.Add(MakeShared<FJsonValueObject>(VarObj));
	}
	ResultObj->SetArrayField(TEXT("variables"), VarsArray);

	// Functions (from FunctionGraphs)
	TArray<TSharedPtr<FJsonValue>> FuncsArray;
	for (UEdGraph* FuncGraph : Blueprint->FunctionGraphs)
	{
		if (!FuncGraph) continue;
		TSharedPtr<FJsonObject> FuncObj = MakeShared<FJsonObject>();
		FuncObj->SetStringField(TEXT("name"), FuncGraph->GetName());
		FuncObj->SetStringField(TEXT("graph_path"), FuncGraph->GetPathName());
		FuncObj->SetNumberField(TEXT("node_count"), FuncGraph->Nodes.Num());

		// Try to get function flags from the generated class
		if (Blueprint->GeneratedClass)
		{
			UFunction* Func = Blueprint->GeneratedClass->FindFunctionByName(FName(*FuncGraph->GetName()));
			if (Func)
			{
				FuncObj->SetBoolField(TEXT("is_pure"), Func->HasAnyFunctionFlags(FUNC_BlueprintPure));
				FuncObj->SetBoolField(TEXT("is_static"), Func->HasAnyFunctionFlags(FUNC_Static));
				FuncObj->SetStringField(TEXT("access"),
					Func->HasAnyFunctionFlags(FUNC_Public) ? TEXT("Public") :
					Func->HasAnyFunctionFlags(FUNC_Protected) ? TEXT("Protected") :
					TEXT("Private"));
			}
		}

		FuncsArray.Add(MakeShared<FJsonValueObject>(FuncObj));
	}
	ResultObj->SetArrayField(TEXT("functions"), FuncsArray);

	// Macros
	TArray<TSharedPtr<FJsonValue>> MacrosArray;
	for (UEdGraph* MacroGraph : Blueprint->MacroGraphs)
	{
		if (!MacroGraph) continue;
		TSharedPtr<FJsonObject> MacroObj = MakeShared<FJsonObject>();
		MacroObj->SetStringField(TEXT("name"), MacroGraph->GetName());
		MacroObj->SetStringField(TEXT("graph_path"), MacroGraph->GetPathName());
		MacroObj->SetNumberField(TEXT("node_count"), MacroGraph->Nodes.Num());
		MacrosArray.Add(MakeShared<FJsonValueObject>(MacroObj));
	}
	if (MacrosArray.Num() > 0)
	{
		ResultObj->SetArrayField(TEXT("macros"), MacrosArray);
	}

	// Interfaces
	TArray<TSharedPtr<FJsonValue>> InterfacesArray;
	for (const FBPInterfaceDescription& Iface : Blueprint->ImplementedInterfaces)
	{
		if (!Iface.Interface) continue;
		TSharedPtr<FJsonObject> IfObj = MakeShared<FJsonObject>();
		IfObj->SetStringField(TEXT("name"), Iface.Interface->GetName());
		IfObj->SetStringField(TEXT("path"), Iface.Interface->GetPathName());
		InterfacesArray.Add(MakeShared<FJsonValueObject>(IfObj));
	}
	if (InterfacesArray.Num() > 0)
	{
		ResultObj->SetArrayField(TEXT("interfaces"), InterfacesArray);
	}

	// Components (SCS)
	if (Blueprint->SimpleConstructionScript)
	{
		TArray<TSharedPtr<FJsonValue>> CompsArray;
		TArray<USCS_Node*> AllNodes = Blueprint->SimpleConstructionScript->GetAllNodes();
		for (USCS_Node* SCSNode : AllNodes)
		{
			if (!SCSNode) continue;
			TSharedPtr<FJsonObject> CompObj = MakeShared<FJsonObject>();
			CompObj->SetStringField(TEXT("name"), SCSNode->GetVariableName().ToString());
			CompObj->SetStringField(TEXT("class"), SCSNode->ComponentClass ? SCSNode->ComponentClass->GetName() : TEXT(""));
			if (SCSNode->ParentComponentOrVariableName != NAME_None)
			{
				CompObj->SetStringField(TEXT("parent"), SCSNode->ParentComponentOrVariableName.ToString());
			}
			CompsArray.Add(MakeShared<FJsonValueObject>(CompObj));
		}
		ResultObj->SetArrayField(TEXT("components"), CompsArray);
	}

	// Graphs overview
	TArray<TSharedPtr<FJsonValue>> GraphsArray;
	auto AddGraphs = [&](const auto& Arr, const FString& Category)
	{
		for (UEdGraph* G : Arr)
		{
			if (!G) continue;
			TSharedPtr<FJsonObject> GO = MakeShared<FJsonObject>();
			GO->SetStringField(TEXT("name"), G->GetName());
			GO->SetStringField(TEXT("path"), G->GetPathName());
			GO->SetStringField(TEXT("class"), G->GetClass()->GetName());
			GO->SetStringField(TEXT("category"), Category);
			GO->SetNumberField(TEXT("node_count"), G->Nodes.Num());
			GraphsArray.Add(MakeShared<FJsonValueObject>(GO));
		}
	};
	AddGraphs(Blueprint->UbergraphPages, TEXT("UbergraphPages"));
	AddGraphs(Blueprint->FunctionGraphs, TEXT("FunctionGraphs"));
	AddGraphs(Blueprint->MacroGraphs, TEXT("MacroGraphs"));
	AddGraphs(Blueprint->DelegateSignatureGraphs, TEXT("DelegateSignatureGraphs"));
	AddGraphs(Blueprint->EventGraphs, TEXT("EventGraphs"));

	// Also scan sub-objects for any other graphs (AnimGraph, etc.)
	TArray<UObject*> SubObjects;
	GetObjectsWithOuter(Asset, SubObjects, false);
	TSet<FString> SeenPaths;
	for (const auto& GA : GraphsArray)
	{
		SeenPaths.Add(GA->AsObject()->GetStringField(TEXT("path")));
	}
	for (UObject* Obj : SubObjects)
	{
		UEdGraph* G = Cast<UEdGraph>(Obj);
		if (!G) continue;
		FString GPath = G->GetPathName();
		if (SeenPaths.Contains(GPath)) continue;
		TSharedPtr<FJsonObject> GO = MakeShared<FJsonObject>();
		GO->SetStringField(TEXT("name"), G->GetName());
		GO->SetStringField(TEXT("path"), GPath);
		GO->SetStringField(TEXT("class"), G->GetClass()->GetName());
		GO->SetStringField(TEXT("category"), TEXT("Other"));
		GO->SetNumberField(TEXT("node_count"), G->Nodes.Num());
		GraphsArray.Add(MakeShared<FJsonValueObject>(GO));
	}
	ResultObj->SetArrayField(TEXT("graphs"), GraphsArray);

	return FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj);
}
