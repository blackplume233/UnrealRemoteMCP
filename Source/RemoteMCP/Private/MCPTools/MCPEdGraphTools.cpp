// ─────────────────── HandleDeleteGraphNode ───────────────────

FJsonObjectParameter UMCPEdGraphTools::HandleDeleteGraphNode(const FJsonObjectParameter& Params)
{
	FString GraphPath;
	if (!Params->TryGetStringField(TEXT("graph_path"), GraphPath))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'graph_path' parameter"));
	}

	FString NodeGuid, NodeName, NodePath;
	Params->TryGetStringField(TEXT("node_guid"), NodeGuid);
	Params->TryGetStringField(TEXT("node_name"), NodeName);
	Params->TryGetStringField(TEXT("node_path"), NodePath);

	FString AutoSavePath;
	Params->TryGetStringField(TEXT("auto_save_asset_path"), AutoSavePath);

	UEdGraph* Graph = LoadObject<UEdGraph>(nullptr, *GraphPath);
	if (!Graph)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Graph not found: %s"), *GraphPath));
	}

	UEdGraphNode* Node = FindNodeInGraph(Graph, NodeGuid, NodeName, NodePath);
	if (!Node)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Node not found"));
	}

	FString RemovedNodePath = Node->GetPathName();
	Graph->Modify();

	// Break all pin links first
	Node->BreakAllNodeLinks();
	Graph->RemoveNode(Node);

	if (!AutoSavePath.IsEmpty())
	{
		SaveAssetIfNeeded(AutoSavePath);
	}

	TSharedPtr<FJsonObject> ResultObj = MakeShared<FJsonObject>();
	ResultObj->SetStringField(TEXT("removed_node"), RemovedNodePath);
	return FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj);
}

// ─────────────────── HandleSetNodeProperties ───────────────────

FJsonObjectParameter UMCPEdGraphTools::HandleSetNodeProperties(const FJsonObjectParameter& Params)
{
	FString GraphPath;
	if (!Params->TryGetStringField(TEXT("graph_path"), GraphPath))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'graph_path' parameter"));
	}

	FString NodeGuid, NodeName, NodePath;
	Params->TryGetStringField(TEXT("node_guid"), NodeGuid);
	Params->TryGetStringField(TEXT("node_name"), NodeName);
	Params->TryGetStringField(TEXT("node_path"), NodePath);

	const TSharedPtr<FJsonObject>* PropertiesPtr = nullptr;
	if (!Params->TryGetObjectField(TEXT("properties"), PropertiesPtr) || !PropertiesPtr || !PropertiesPtr->IsValid())
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing or invalid 'properties' parameter"));
	}

	FString AutoSavePath;
	Params->TryGetStringField(TEXT("auto_save_asset_path"), AutoSavePath);

	UEdGraph* Graph = LoadObject<UEdGraph>(nullptr, *GraphPath);
	if (!Graph)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Graph not found: %s"), *GraphPath));
	}

	UEdGraphNode* Node = FindNodeInGraph(Graph, NodeGuid, NodeName, NodePath);
	if (!Node)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Node not found"));
	}

	Node->Modify();

	TSharedPtr<FJsonObject> AppliedObj = MakeShared<FJsonObject>();
	TSharedPtr<FJsonObject> ErrorsObj = MakeShared<FJsonObject>();

	for (const auto& Pair : (*PropertiesPtr)->Values)
	{
		FString ErrorMessage;
		bool bOk = FUnrealMCPCommonUtils::SetObjectProperty(Node, Pair.Key, Pair.Value, ErrorMessage);
		if (bOk)
		{
			AppliedObj->SetField(Pair.Key, Pair.Value);
		}
		else
		{
			ErrorsObj->SetStringField(Pair.Key, ErrorMessage);
		}
	}

	if (!AutoSavePath.IsEmpty())
	{
		SaveAssetIfNeeded(AutoSavePath);
	}

	TSharedPtr<FJsonObject> ResultObj = MakeShared<FJsonObject>();
	ResultObj->SetObjectField(TEXT("node"), SerializeNode(Node, false));
	ResultObj->SetObjectField(TEXT("applied"), AppliedObj);
	ResultObj->SetObjectField(TEXT("errors"), ErrorsObj);
	return FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj);
}

// ─────────────────── HandleListGraphLinks ───────────────────

FJsonObjectParameter UMCPEdGraphTools::HandleListGraphLinks(const FJsonObjectParameter& Params)
{
	FString GraphPath;
	if (!Params->TryGetStringField(TEXT("graph_path"), GraphPath))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'graph_path' parameter"));
	}

	UEdGraph* Graph = LoadObject<UEdGraph>(nullptr, *GraphPath);
	if (!Graph)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Graph not found: %s"), *GraphPath));
	}

	TSet<FString> Seen;
	TArray<TSharedPtr<FJsonValue>> LinksArray;

	for (UEdGraphNode* Node : Graph->Nodes)
	{
		if (!Node)
		{
			continue;
		}
		FString NodeGuid = Node->NodeGuid.ToString();

		for (UEdGraphPin* Pin : Node->Pins)
		{
			if (!Pin)
			{
				continue;
			}
			FString PinName = Pin->PinName.ToString();

			for (UEdGraphPin* LinkedPin : Pin->LinkedTo)
			{
				if (!LinkedPin || !LinkedPin->GetOwningNode())
				{
					continue;
				}
				FString OtherGuid = LinkedPin->GetOwningNode()->NodeGuid.ToString();
				FString OtherPinName = LinkedPin->PinName.ToString();

				// Deduplicate (unordered pair)
				FString KeyA = FString::Printf(TEXT("%s:%s"), *NodeGuid, *PinName);
				FString KeyB = FString::Printf(TEXT("%s:%s"), *OtherGuid, *OtherPinName);
				FString Key = (KeyA < KeyB)
					? FString::Printf(TEXT("%s|%s"), *KeyA, *KeyB)
					: FString::Printf(TEXT("%s|%s"), *KeyB, *KeyA);
				if (Seen.Contains(Key))
				{
					continue;
				}
				Seen.Add(Key);

				TSharedPtr<FJsonObject> AObj = MakeShared<FJsonObject>();
				AObj->SetStringField(TEXT("node_guid"), NodeGuid);
				AObj->SetStringField(TEXT("node_name"), Node->GetName());
				AObj->SetStringField(TEXT("pin"), PinName);

				TSharedPtr<FJsonObject> BObj = MakeShared<FJsonObject>();
				BObj->SetStringField(TEXT("node_guid"), OtherGuid);
				BObj->SetStringField(TEXT("node_name"), LinkedPin->GetOwningNode()->GetName());
				BObj->SetStringField(TEXT("pin"), OtherPinName);

				TSharedPtr<FJsonObject> LinkObj = MakeShared<FJsonObject>();
				LinkObj->SetObjectField(TEXT("a"), AObj);
				LinkObj->SetObjectField(TEXT("b"), BObj);
				LinksArray.Add(MakeShared<FJsonValueObject>(LinkObj));
			}
		}
	}

	TSharedPtr<FJsonObject> ResultObj = MakeShared<FJsonObject>();
	ResultObj->SetStringField(TEXT("graph_path"), GraphPath);
	ResultObj->SetNumberField(TEXT("link_count"), LinksArray.Num());
	ResultObj->SetArrayField(TEXT("links"), LinksArray);
	return FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj);
}

// ─────────────────── HandleConnectPins ───────────────────

FJsonObjectParameter UMCPEdGraphTools::HandleConnectPins(const FJsonObjectParameter& Params)
{
	FString GraphPath;
	if (!Params->TryGetStringField(TEXT("graph_path"), GraphPath))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'graph_path' parameter"));
	}

	FString FromNodeGuid, FromPinName, ToNodeGuid, ToPinName;
	if (!Params->TryGetStringField(TEXT("from_node_guid"), FromNodeGuid))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'from_node_guid' parameter"));
	}
	if (!Params->TryGetStringField(TEXT("from_pin"), FromPinName))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'from_pin' parameter"));
	}
	if (!Params->TryGetStringField(TEXT("to_node_guid"), ToNodeGuid))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'to_node_guid' parameter"));
	}
	if (!Params->TryGetStringField(TEXT("to_pin"), ToPinName))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'to_pin' parameter"));
	}

	FString AutoSavePath;
	Params->TryGetStringField(TEXT("auto_save_asset_path"), AutoSavePath);

	UEdGraph* Graph = LoadObject<UEdGraph>(nullptr, *GraphPath);
	if (!Graph)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Graph not found: %s"), *GraphPath));
	}

	UEdGraphNode* FromNode = FindNodeInGraph(Graph, FromNodeGuid, TEXT(""), TEXT(""));
	UEdGraphNode* ToNode = FindNodeInGraph(Graph, ToNodeGuid, TEXT(""), TEXT(""));
	if (!FromNode || !ToNode)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("From/To node not found"));
	}

	UEdGraphPin* FromPin = FindPinOnNode(FromNode, FromPinName);
	UEdGraphPin* ToPin = FindPinOnNode(ToNode, ToPinName);
	if (!FromPin || !ToPin)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("From/To pin not found"));
	}

	bool bOk = false;

	// Try Schema first
	const UEdGraphSchema* Schema = Graph->GetSchema();
	if (Schema)
	{
		bOk = Schema->TryCreateConnection(FromPin, ToPin);
	}

	if (!bOk)
	{
		// Fallback: direct link
		FromPin->MakeLinkTo(ToPin);
		bOk = FromPin->LinkedTo.Contains(ToPin);
	}

	if (!AutoSavePath.IsEmpty())
	{
		SaveAssetIfNeeded(AutoSavePath);
	}

	TSharedPtr<FJsonObject> ResultObj = MakeShared<FJsonObject>();
	ResultObj->SetBoolField(TEXT("connected"), bOk);
	return bOk
		? FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj)
		: FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Failed to connect pins"));
}

// ─────────────────── HandleDisconnectPin ───────────────────

FJsonObjectParameter UMCPEdGraphTools::HandleDisconnectPin(const FJsonObjectParameter& Params)
{
	FString GraphPath;
	if (!Params->TryGetStringField(TEXT("graph_path"), GraphPath))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'graph_path' parameter"));
	}

	FString NodeGuid, PinName;
	if (!Params->TryGetStringField(TEXT("node_guid"), NodeGuid))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'node_guid' parameter"));
	}
	if (!Params->TryGetStringField(TEXT("pin_name"), PinName))
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Missing 'pin_name' parameter"));
	}

	FString AutoSavePath;
	Params->TryGetStringField(TEXT("auto_save_asset_path"), AutoSavePath);

	UEdGraph* Graph = LoadObject<UEdGraph>(nullptr, *GraphPath);
	if (!Graph)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Graph not found: %s"), *GraphPath));
	}

	UEdGraphNode* Node = FindNodeInGraph(Graph, NodeGuid, TEXT(""), TEXT(""));
	if (!Node)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(TEXT("Node not found"));
	}

	UEdGraphPin* Pin = FindPinOnNode(Node, PinName);
	if (!Pin)
	{
		return FUnrealMCPCommonUtils::CreateErrorResponse(FString::Printf(TEXT("Pin '%s' not found"), *PinName));
	}

	Graph->Modify();
	int32 BrokenCount = Pin->LinkedTo.Num();
	Pin->BreakAllPinLinks();

	if (!AutoSavePath.IsEmpty())
	{
		SaveAssetIfNeeded(AutoSavePath);
	}

	TSharedPtr<FJsonObject> ResultObj = MakeShared<FJsonObject>();
	ResultObj->SetNumberField(TEXT("broken_link_count"), BrokenCount);
	return FUnrealMCPCommonUtils::CreateSuccessResponse(ResultObj);
}

